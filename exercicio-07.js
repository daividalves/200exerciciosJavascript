/*
 Declare três variáveis e atribua valores numéricos a elas. 
 
 Use operadores de comparação para comparar os valores entre as variáveis. Imprima os resultados.

*/


// Declarando e atribuindo valores numéricos
const a = 10;
const b = 25;
const c = 10;

// Operador de igualdade (==)
console.log(`a == b: ${a == b}`); // Saída: false
console.log(`a == c: ${a == c}`); // Saída: true

// Operador de igualdade estrita (===)
console.log(`a === c: ${a === c}`); // Saída: true

// Operador de desigualdade (!=)
console.log(`a != b: ${a != b}`); // Saída: true
console.log(`a != c: ${a != c}`); // Saída: false

// Operador de desigualdade estrita (!==)
console.log(`a !== c: ${a !== c}`); // Saída: false

// Operador "maior que" (>)
console.log(`b > a: ${b > a}`); // Saída: true
console.log(`a > b: ${a > b}`); // Saída: false

// Operador "menor que" (<)
console.log(`a < b: ${a < b}`); // Saída: true
console.log(`b < a: ${b < a}`); // Saída: false

// Operador "maior ou igual a" (>=)
console.log(`a >= c: ${a >= c}`); // Saída: true
console.log(`b >= a: ${b >= a}`); // Saída: true

// Operador "menor ou igual a" (<=)
console.log(`a <= c: ${a <= c}`); // Saída: true
console.log(`a <= b: ${a <= b}`); // Saída: true


